class Map:
    def __init__(self, game):
        self.game = game
        self.mini_map = mini_map
        self.world_map = {}
        self.rows = len(self.mini_map)
        self.cols = len(self.mini_map[0])
        self.get_map()

    def get_map(self):
        for j, row in enumerate(self.mini_map):
            for i, value in enumerate(row):
                if value:
                    self.world_map[(i, j)] = value

    def draw(self):
        startx = 0
        starty = HEIGHT - (self.rows * MAP_SCALE)
        for ny in range(self.rows):
            for nx in range(self.cols):
                if (nx, ny) in self.world_map:
                    col = (57, 255, 20)
                else:
                    col = (0,0,0)
                pg.draw.rect(self.game.screen, col, (nx * MAP_SCALE + startx, ny * MAP_SCALE + starty, MAP_SCALE, MAP_SCALE), MAP_SCALE)
        px, py = self.game.player.map_pos
        pg.draw.rect(self.game.screen, (254, 52, 126), (px * MAP_SCALE + startx, py * MAP_SCALE + starty, MAP_SCALE, MAP_SCALE), MAP_SCALE)
